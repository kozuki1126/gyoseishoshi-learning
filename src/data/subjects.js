// 単元データを取得する関数
export function getUnit(id) {
  // 単元データの作成
  const unitData = {};
  
  // すべての科目から単元を検索
  for (const subject of subjects) {
    for (const unit of subject.units) {
      if (unit.id.toString() === id.toString()) {
        // 基本的な単元情報
        unitData.id = unit.id;
        unitData.title = unit.title;
        unitData.subjectId = subject.id;
        unitData.subjectName = subject.title;
        unitData.description = `${subject.title}の${unit.title.split('.')[0]}番目の単元です。`;
        
        // 関連単元（同じ科目の他の単元）
        const relatedUnits = subject.units
          .filter(u => u.id !== unit.id)
          .slice(0, 3)
          .map(u => u.id);
        unitData.relatedUnits = relatedUnits;
        
        // 前後の単元
        const currentIndex = subject.units.findIndex(u => u.id === unit.id);
        unitData.previousUnit = currentIndex > 0 ? subject.units[currentIndex - 1].id : null;
        unitData.nextUnit = currentIndex < subject.units.length - 1 ? subject.units[currentIndex + 1].id : null;
        
        // メディアリンク（ダミー）
        unitData.audioUrl = `/audio/${unit.id}.mp3`;
        unitData.pdfUrl = `/pdf/${unit.id}.pdf`;
        
        // ダミーのコンテンツ（実際のアプリではAPIから取得）
        unitData.content = `
          <h2>${unit.title}</h2>
          <p>このコンテンツは準備中です。もうしばらくお待ちください。</p>
          <h3>このトピックの主なポイント</h3>
          <ul>
            <li>ポイント1: 詳細は準備中です</li>
            <li>ポイント2: 詳細は準備中です</li>
            <li>ポイント3: 詳細は準備中です</li>
          </ul>
          <p>今後、このページには詳細な解説と例題が追加される予定です。</p>
        `;
        
        // クイズ（ダミー）
        unitData.quizzes = [
          {
            question: 'この単元に関する質問1?',
            options: [
              '選択肢A',
              '選択肢B',
              '選択肢C',
              '選択肢D'
            ],
            answer: 0
          },
          {
            question: 'この単元に関する質問2?',
            options: [
              '選択肢A',
              '選択肢B',
              '選択肢C',
              '選択肢D'
            ],
            answer: 1
          }
        ];
        
        break;
      }
    }
  }
  
  return unitData;
}